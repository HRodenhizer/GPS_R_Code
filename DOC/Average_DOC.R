### Load data #################################################################################################
doc <- read.csv('C:/Users/Heidi Rodenhizer/Documents/School/NAU/Schuur Lab/DOC/2007_2017_DOC_alldata.csv')
###############################################################################################################

### Clean and average data ####################################################################################
avg.doc <- doc %>%
  filter(Experiment == 'CiPEHRDryPEHR') %>%
  group_by(Year, Treatment) %>%
  dplyr::summarise(avgdoc = mean(DOCmgl, na.rm = TRUE),
            sedoc = sd(DOCmgl, na.rm = TRUE)/sqrt(n()))
